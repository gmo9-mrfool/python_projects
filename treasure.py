def back():
    user = input("Left or Right?\n")
    user = user.lower()
    if user == 'left':
        second = input('Swim or wait?\n')
        second = second.lower()
        if second == 'wait':
            while True:
                door = input('Which door? Red,Blue or Yellow\n')
                door = door.lower()
                if door == 'red':
                    print('Burned by fire.\nGame Over.')
                    quit()
                elif door == 'blue':
                    print('Eaten by beasts.\nGame over.')
                    quit()
                elif door == 'yellow':
                    print('You Win!')
                    quit()
                else:
                    print('Invalid door choose again')
        else:
           print('Attacked by sharks.\nGame over.')
           quit()        
    
    elif user == 'right':

        print('Found a treasure map showing a hole.')
        hole = input('Enter the hole for shortcut? Y or N\n')
        hole = hole.lower()
        if hole == 'y':
            print('Game over! You died!!')
            quit()
        
            
        elif hole == 'n':

            print('You decide to return to the previous path')
            back()  
    else:
        back()

back()
